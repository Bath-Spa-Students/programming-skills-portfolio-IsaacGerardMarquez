# Exercise 3: Your Own List (Think of your favorite mode of transportation, such as a motorcycle or a car, and make a list that stores several examples. Use your list to print a series of statements about these items, such as “I would like to own a Honda motorcycle.”)

# Creating a list named "Cars" that stores several examples of car I would like to someday be able to own and drive.
Cars = ["Honda Civic", "Dodge Charger", "Ford Raptor", "BMW X6", "Mini Cooper", "Tesla Model Y", "Kia Stinger"]

# Creating a for loop to iterate over each element in the list named "Cars".
for Cars in Cars:

# Using an f-string to display a series of statements (each personalized) for each car in the list named "Cars".
    print(f"I would like to someday be able to own and drive a {Cars}.")