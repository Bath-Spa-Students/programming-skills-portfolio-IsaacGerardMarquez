#Exercise 1: Variables (Assign a message to a variable, and print that message. Then change the value of the variable to a new message, and print the new message.)

# Assigning a message to a variable.
message = "Learning Python is challenging yet amusing."
# Printing the message.
print(message)
# Changing the value of the variable to a new message.
message = "It's truly enjoyable!"
# Printing the new message.
print(message)