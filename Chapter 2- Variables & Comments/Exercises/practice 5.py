#Q5: Write a python program that stores fruit names in a list named fruits. Unpack the list into three variables x, y and z and then print the values of each variable.

# Creating a list named "fruits" that stores fruit names.
fruits = ["Strawberry", "Blueberry", "Raspberry"]

# Since the list named "fruits" has three elements, each variable (x, y, and z) will be assigned with one element from the list in the order they appear.
# Unpacking the list into three variables x, y and z and then printing the values of each variable.
x, y, z = fruits
print (x)
print (y)
print (z)