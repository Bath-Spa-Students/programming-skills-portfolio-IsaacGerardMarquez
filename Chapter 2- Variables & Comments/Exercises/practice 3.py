#Q3: Write a python program that takes an input 5 from user and then type cast those values to string, int and float in the separate variables. Print all the variables.

# The str() function converts the value inside the parentheses (which is 5) into a string.
String = str(5)

# The int() function converts the value inside the parentheses (which is 5) into an integer.
Integer = int(5)

# The float() function converts the value inside the parentheses (which is 5) into a floating-point number.
Float = float(5)

# Using print() function to display the value of the variable "String".
print(String)

# Using print() function to display the value of the variable "Integer".
print(Integer)

# Using print() function to display the value of the variable "Float".
print(Float)