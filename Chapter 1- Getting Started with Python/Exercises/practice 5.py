# Practice 5: Write four string in different variables and print the output as one string. (String Concatenation)

# Writing four strings in different variables. Each variable is assigned with a specific string value.
string1 = "Good morning!"
string2 = "My"
string3 = "Name is"
string4 = "Isaac Marquez."

# Concatenating the four strings in different variables together with the use of the "+" operator.
new_string= string1 + " " + string2 + " " + string3 + " " + string4

# Using the print() function to print or display the value that is stored in the variable named "new_string".
print(new_string)