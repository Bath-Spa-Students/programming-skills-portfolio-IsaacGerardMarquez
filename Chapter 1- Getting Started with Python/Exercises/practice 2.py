#Create a program to output "hi" 50 times, without any separator

# In this program, "hi" is a string that represents the word "hi".
# "*" is used as the operator for string repetition.
# In this instance, "hi"*50 will concatenate the string "hi" together 50 times.
# Lastly, the print() function will then output or display "hi" 50 times, without any separator.
print ("hi"*50)