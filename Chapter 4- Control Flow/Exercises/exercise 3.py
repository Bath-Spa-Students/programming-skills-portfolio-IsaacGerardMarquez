# Exercise 3: Alien Colors #3 (Turn your if-else chain from Exercise 5-4 into an if-elifelse chain.)
# • If the alien is green, print a message that the player earned 5 points.
# • If the alien is yellow, print a message that the player earned 10 points.
# • If the alien is red, print a message that the player earned 15 points.
# • Write three versions of this program, making sure each message is printed for the appropriate color alien.

# Program-1-Green
# The variable named "alien_color" is assigned to have the value of "green".
alien_color = "green"

if alien_color == "green": # Elif statement will run if the value of alien_color is equal to "green".
    print ("Congratulations! You just earned 5 points!")
elif alien_color == "yellow": # Elif statement will run if the value of alien_color is equal to "yellow".
    print ("Congratulations! You just earned 10 points!")
else: # Else statement will run if the value of alien_color is not equal to either "green" and "yellow".
    print ("Congratulations! You just earned 15 points!")

