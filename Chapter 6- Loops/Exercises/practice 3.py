# Practice 3: Write a Python program that uses a loop to find the sum of all the numbers from 1 to 100.

# Initialize the variable "Sum_of_all_the_numbers" with 0 (zero).
Sum_of_all_the_numbers = 0 

# Using a for loop to iterate over the list of numbers from 1 to 100.
for i in range(1, 101): 
  Sum_of_all_the_numbers += i 
  
# Printing or displaying the "Sum_of_all_the_numbers" which shows the sum of all the numbers from 1 to 100.
print(Sum_of_all_the_numbers) 